package com.colecctions.practice;

public class Studentarray {

		int id;
		String name;
		public Studentarray(int id, String name) {
			super();
			this.id = id;
			this.name = name;
		}
		@Override
		public String toString() {
			return "Student [id=" + id + ", name=" + name + "]";
		}
		
	

	

}
